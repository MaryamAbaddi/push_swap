#include "push_swap.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	a;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	a = '\n';
	write(fd, s, i);
	write(fd, &a, 1);
}
